#Arithmetic operators are used to perform mathematical operations
a=10
b=5
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)

#Assignment operator is used to assign a value to a variable.
x=10
x+=10
print(x)
x-=5
print(x)
x*=2
print(x)
x//=7
print(x)
print(x)
x%=2
print(x)

#Logical operator
a=5
b=10
print(a>2 and b<15)
print(a<b)
print(a>5 or b<=10)
print(not(a))
print(a<8 and b>5) 

# Bitwise operator
p=8
q=10
print(p&q)
print(p|q)
print(p^q)
print(~p)


#Membership operator
names=["Geetha","Veda","Kavya"]
print("Geetha" in names)
print("Chinni" in names)



